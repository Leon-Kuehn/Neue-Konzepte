type TranslationLanguage = keyof typeof translations;

function resolveTranslationLanguage(language: AppLanguage): TranslationLanguage {
  if (language === "de" || language === "fr" || language === "es") {
    return language;
  }

  return "en";
}

export function translate(language: AppLanguage, key: TranslationKey): string {
  const selectedLanguage = resolveTranslationLanguage(language);
  return translations[selectedLanguage][key] ?? translations.en[key];
}
